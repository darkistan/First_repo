def add(a, b):
    """Повертає суму двох чисел (короткий рядковий опис функції)."""
    return a + b

# Початкові дані (однорядковий коментар пояснює дію)
x = 5
y = 7

# Обчислення суми
result = add(x, y)

# Виведення результату
print(f"{x} + {y} = {result}")

