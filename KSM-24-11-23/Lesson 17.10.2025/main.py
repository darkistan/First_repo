# Тема: Оператори, пріоритети операторів
# Завдання 1. Обчислення виразів
# Мета: навчитися визначати порядок виконання арифметичних операцій у Python

# Запитуємо три числа у користувача
a = float(input("Введіть число a: "))
b = float(input("Введіть число b: "))
c = float(input("Введіть число c: "))

# Обчислення виразів згідно з пріоритетами операторів
expr1 = a + b * c
expr2 = (a + b) * c
expr3 = a + b / c
expr4 = (a + b) / c

# Виведення результатів
print("\nРезультати обчислень:")
print(f"a + b * c = {expr1}")
print(f"(a + b) * c = {expr2}")
print(f"a + b / c = {expr3}")
print(f"(a + b) / c = {expr4}")

