# Програма для контролю щоденної активності 
# запитуємо мету по кроках
goal = int(input("Введіть вашу ціль по кроках: "))

# змінна для підрахунку зроблених кроків
total_steps = 0

# цикл для багаторазового введення кроків
while True:
    steps = int(input("Введіть кількість зроблених кроків (0 - завершити): "))
    if steps == 0:
        break
    total_steps += steps  # додаємо кроки до загальної суми

# розрахунок відсотка виконання
percent = (total_steps / goal) * 100

# вивід результатів
if total_steps >= goal:
    print("Мета досягнута! Вітаємо!")
else:
    remaining = goal - total_steps
    print("Залишилось до мети:", remaining, "кроків.")

print("Виконано", round(percent, 2), "% від плану.")
