# Програма "Розрахунок вартості таксі"

# Введення відстані поїздки в кілометрах
distance_km = float(input("Введіть відстань поїздки (км): "))

# Введення часу очікування в хвилинах
wait_min = float(input("Введіть час очікування (хв): "))

# Фіксована вартість подачі таксі
base_cost = 40

# Вартість за 1 кілометр
cost_per_km = 8

# Вартість за 1 хвилину очікування
cost_per_min = 2

# Обчислення загальної вартості поїздки за формулою
trip_cost = base_cost + cost_per_km * distance_km + cost_per_min * wait_min

# Виведення результату
print("Вартість поїздки:", trip_cost, "грн")
