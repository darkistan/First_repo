# Завдання 1. Функція для підрахунку літер 'а' та обробка рядка
# - Порахувати кількість букв 'а' (кирилична, незалежно від регістру)
# - Замінити пробіли на підкреслення і вивести довжини до/після заміни

# Функція для підрахунку кількості букв 'а' (кирилична, незалежно від регістру)
def count_letters_a(s, include_latin=False):
    # Перетворюємо рядок у нижній регістр, щоб не розрізняти великі та малі літери
    s_lower = s.lower()
    # Рахуємо кількість кириличних 'а' у рядку
    count_cyr = s_lower.count('а')
    # Якщо параметр include_latin=True — також рахуємо латинську 'a'
    if include_latin:
        count_cyr += s_lower.count('a')
    # Повертаємо загальну кількість знайдених літер
    return count_cyr

# Головна функція програми
def main():
    # Зчитуємо рядок, введений користувачем з клавіатури
    s = input("Введіть рядок: ")
    # Обчислюємо довжину початкового рядка
    len_before = len(s)

    # Замінюємо всі пробіли у рядку на символ підкреслення "_"
    s_replaced = s.replace(' ', '_')
    # Обчислюємо довжину рядка після заміни (в даному випадку довжина не зміниться)
    len_after = len(s_replaced)

    # Викликаємо функцію для підрахунку кількості літер 'а' у введеному рядку
    # include_latin=False означає, що рахуємо тільки кириличну 'а'
    count_a = count_letters_a(s, include_latin=False)

    # Виводимо роздільник для зручності читання результату
    print("\nРезультат:")
    # Виводимо змінений рядок (з підкресленнями замість пробілів)
    print("Змінений рядок: ", s_replaced)
    # Виводимо кількість входжень літери 'а' (незалежно від регістру)
    print("Кількість входжень літери 'а' (незалежно від регістру):", count_a)
    # Виводимо довжину рядка до заміни
    print(f"Довжина рядка до заміни: {len_before}")
    # Виводимо довжину рядка після заміни
    print(f"Довжина рядка після заміни: {len_after}")

# Виконуємо головну функцію, якщо файл запускається напряму
if __name__ == "__main__":
    main()
