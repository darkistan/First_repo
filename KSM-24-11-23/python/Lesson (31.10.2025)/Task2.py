# програма визначає пору року за номером місяця

month = int(input("Введіть номер місяця (1-12): "))

# перевірка діапазону та визначення пори року
if 1 <= month <= 3:
    print("Зима")
elif 4 <= month <= 6:
    print("Весна")
elif 7 <= month <= 9:
    print("Літо")
elif 10 <= month <= 12:
    print("Осінь")
else:
    print("Некоректний номер місяця")
