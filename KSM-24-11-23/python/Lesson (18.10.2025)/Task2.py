# програма для обчислення довжини кола

import math  # підключення модуля math

PI = math.pi  # створення константи PI

r = float(input("Введіть радіус кола: "))  # введення радіуса користувачем

if r > 0:  # перевірка чи радіус додатній
    l = 2 * PI * r  # обчислення довжини кола
    print("Довжина кола =", l)  # виведення результату
else:  # якщо радіус не додатній
    print("Радіус має бути більший за нуль")  # повідомлення про помилку
