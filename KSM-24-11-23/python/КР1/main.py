# Варіант 2. Дано три числа. 
# Визначити найбільше з них. Якщо найбільших чисел кілька 
# – вивести повідомлення про це. Обчислити різницю між найбільшим і найменшим.


# три числа
a = float(input("Введи перше число: "))
b = float(input("Введи друге число: "))
c = float(input("Введи третє число: "))

# Помістимо в список 
nums = [a, b, c]

max_val = max(nums)   # найбільше
min_val = min(nums)   # найменше

# Перевіряємо, чи найбільших чисел кілька
if nums.count(max_val) > 1:
    print("Найбільших чисел кілька (значення = {})".format(max_val))
else:
    print("Найбільше число:", max_val)

# Різниця між найбільшим і найменшим
diff = max_val - min_val
print("Різниця між найбільшим і найменшим:", diff)
