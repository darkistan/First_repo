# –Ü–º–ø–æ—Ä—Ç—É—î–º–æ –º–æ–¥—É–ª—å datetime –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ –¥–∞—Ç–∞–º–∏
import datetime

# –í–∏–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤—É –ø—Ä–æ–≥—Ä–∞–º–∏
print("–ü—Ä–æ–≥—Ä–∞–º–∞ '–í—ñ–¥–ª—ñ–∫ –¥–Ω—ñ–≤ –¥–æ —Å–≤—è—Ç–∞'")
print("--------------------------------")

# –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å –ø–æ—Ç–æ—á–Ω—É –¥–∞—Ç—É
day = int(input("–í–≤–µ–¥—ñ—Ç—å –ø–æ—Ç–æ—á–Ω–∏–π –¥–µ–Ω—å: "))
month = int(input("–í–≤–µ–¥—ñ—Ç—å –ø–æ—Ç–æ—á–Ω–∏–π –º—ñ—Å—è—Ü—å (—á–∏—Å–ª–æ–º): "))
year = int(input("–í–≤–µ–¥—ñ—Ç—å –ø–æ—Ç–æ—á–Ω–∏–π —Ä—ñ–∫: "))

# –°—Ç–≤–æ—Ä—é—î–º–æ –æ–±'—î–∫—Ç –¥–∞—Ç–∏ –¥–ª—è –ø–æ—Ç–æ—á–Ω–æ—ó –¥–∞—Ç–∏
current_date = datetime.date(year, month, day)

# –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å –¥–∞—Ç—É —Å–≤—è—Ç–∞
print("\n–í–≤–µ–¥—ñ—Ç—å –¥–∞—Ç—É –≤–∞—à–æ–≥–æ —Å–≤—è—Ç–∞:")
holiday_day = int(input("–î–µ–Ω—å —Å–≤—è—Ç–∞: "))
holiday_month = int(input("–ú—ñ—Å—è—Ü—å —Å–≤—è—Ç–∞ (—á–∏—Å–ª–æ–º): "))
holiday_name = input("–ù–∞–∑–≤–∞ —Å–≤—è—Ç–∞: ")

# –°—Ç–≤–æ—Ä—é—î–º–æ –¥–∞—Ç—É —Å–≤—è—Ç–∞ —É –ø–æ—Ç–æ—á–Ω–æ–º—É —Ä–æ—Ü—ñ
holiday_date = datetime.date(year, holiday_month, holiday_day)

# –Ø–∫—â–æ —Å–≤—è—Ç–æ –≤–∂–µ –º–∏–Ω—É–ª–æ ‚Äî —Ä–∞—Ö—É—î–º–æ –¥–æ —Å–≤—è—Ç–∞ —É –Ω–∞—Å—Ç—É–ø–Ω–æ–º—É —Ä–æ—Ü—ñ
if holiday_date < current_date:
    holiday_date = datetime.date(year + 1, holiday_month, holiday_day)

# –û–±—á–∏—Å–ª—é—î–º–æ —Ä—ñ–∑–Ω–∏—Ü—é –º—ñ–∂ –¥–∞—Ç–∞–º–∏
difference = holiday_date - current_date
days_left = difference.days

# –í–∏–≤–æ–¥–∏–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
print(f"\n–î–æ —Å–≤—è—Ç–∞ '{holiday_name}' –∑–∞–ª–∏—à–∏–ª–æ—Å—å {days_left} –¥–Ω—ñ–≤!")

# –ñ–∞—Ä—Ç—ñ–≤–ª–∏–≤–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
print(f"–ì–æ—Ç—É–π—Å—è –¥–æ {holiday_name.lower()}! üéâ")
