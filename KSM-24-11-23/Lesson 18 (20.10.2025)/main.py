# Програма: Математичні оператори в Python
# Мета: продемонструвати використання основних арифметичних операцій

# Запитуємо у користувача два числа
num1 = float(input("Введіть перше число: "))
num2 = float(input("Введіть друге число: "))

# Виконуємо обчислення
summa = num1 + num2            # додавання
rizn = num1 - num2             # віднімання
mnozh = num1 * num2            # множення
dilennya = num1 / num2         # ділення
ostacha = num1 % num2          # остача від ділення

# Виводимо результати з поясненнями
print("\nРезультати обчислень:")
print(f"{num1} + {num2} = {summa}  -> сума двох чисел")
print(f"{num1} - {num2} = {rizn}  -> різниця (наскільки перше більше або менше другого)")
print(f"{num1} * {num2} = {mnozh}  -> результат множення")
print(f"{num1} / {num2} = {dilennya}  -> частка від ділення першого числа на друге")
print(f"{num1} % {num2} = {ostacha}  -> остача від ділення (що залишилось після ділення)")

