# Програма для обчислення середнього арифметичного двох чисел
print("Введіть перше число: ")
num1 = float(input())

print("Введіть друге число: ")
num2 = float(input())

average = (num1 + num2) / 2

print("Середнє арифметичне дорівнює: " + str(average))

