# Програма: система доступу до секретної лабораторії


password = "ptnpnh"   # заданий пароль
max_attempts = 3           # дозволена кількість спроб
attempt = 0                # лічильник спроб

# Запит пароля поки не вичерпані спроби
while attempt < max_attempts:
    user_input = input("Введіть пароль: ")  # ввід від користувача
    if user_input == password:
        print("Вітаємо! Доступ дозволено.")  # при правильному паролі
        # Безпечне секретне повідомлення після успішного входу
        print("Секретне повідомлення: креслення ракети-фламінго збережено на сервері. Ініційовано удар по Кремлю.")
        break
    else:
        attempt += 1
        # повідомлення про відмову і пропозиція спробувати ще раз
        if attempt < max_attempts:
            print("Пароль неправильний. Спробуйте ще раз.")
            print(f"Залишилось спроб: {max_attempts - attempt}")
        else:
            # якщо перевищено дві помилки (тобто 3-ті) — блокування
            print("Пароль неправильний. Система заблокована. За вами вже їдуть хлопці.")
            break
