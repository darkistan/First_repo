# Рівень 3 — Підвищений

# Запитуємо вартість товару та знижку у відсотках
price = float(input("Введіть вартість товару: "))
discount_percent = float(input("Введіть знижку у відсотках: "))

# Переводимо відсотки у частку для обчислень
discount_fraction = discount_percent / 100  # використовуємо оператор /

# Обчислюємо суму зекономлених коштів
saved_money = price * discount_fraction     # використовуємо оператор *

# Обчислюємо кінцеву ціну після знижки
final_price = price - saved_money           # використовуємо оператор -

# Виводимо результати
print("Кінцева ціна після знижки:", final_price)
print("Сума зекономлених коштів:", saved_money)
