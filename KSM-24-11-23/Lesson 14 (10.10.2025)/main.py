# Програма: "Мандрівка автомобілем"


# Оголошення константи – середня витрата пального (літрів на 100 км)
AVERAGE_CONSUMPTION = 7.0

# Введення даних від користувача
distance = float(input("Введіть відстань у кілометрах: "))
price_per_liter = float(input("Введіть ціну пального за літр: "))

# Запит користувача — чи хоче він змінити стандартну витрату
change_consumption = input("Бажаєте змінити середню витрату пального? (так/ні): ").lower()

# Якщо користувач хоче змінити – вводимо нове значення
if change_consumption == "так":
    consumption = float(input("Введіть нову середню витрату (літрів на 100 км): "))
else:
    consumption = AVERAGE_CONSUMPTION  

# Розрахунок кількості пального для поїздки в один бік
fuel_needed = (distance * consumption) / 100

# Розрахунок вартості поїздки в один бік
trip_cost = fuel_needed * price_per_liter

# Розрахунок вартості поїздки в обидва напрямки
round_trip_cost = trip_cost * 2

# Виведення результатів
print("\nСередня витрата автомобіля:", consumption, "л на 100 км")
print("Необхідно пального:", round(fuel_needed, 2), "л")
print("Вартість поїздки в один бік:", round(trip_cost, 2), "грн")
print("Вартість поїздки в обидва напрямки:", round(round_trip_cost, 2), "грн")

# Повідомлення, якщо відстань перевищує 500 км
if distance > 500:
    print("Подбайте про відпочинок під час тривалої поїздки!")
