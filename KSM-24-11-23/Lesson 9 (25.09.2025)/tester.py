# Програма-тестер крученої пари Ethernet за стандартом T568B

# 1. Запропонувати користувачу ввести послідовність із восьми чисел від 1 до 8 через пробіл
user_input = input("Введіть послідовність із восьми чисел від 1 до 8 через пробіл: ")

# Перетворюємо введений рядок у список цілих чисел
entered = list(map(int, user_input.split()))

# 2. Зберегти еталонну послідовність T568B як масив 1 2 3 4 5 6 7 8
t568b = [1, 2, 3, 4, 5, 6, 7, 8]

# 3. Порівняти введену послідовність з еталоном
if entered == t568b:
    # 4. Якщо послідовність співпадає вивести повідомлення
    print("Кабель зєднано правильно")
else:
    # 5. Якщо є помилки показати які саме жили зєднані невірно
    print("Знайдено помилки у підключенні:")
    # Проходимо по всіх 8 позиціях і показуємо невірні
    for i in range(8):
        if entered[i] != t568b[i]:
            print(f"Позиція {i+1}: введено {entered[i]}, треба {t568b[i]}")
