# запитуємо температуру у користувача
temp_str = input("Введіть температуру тіла (°C): ")

# перетворюємо рядок у число
try:
    temp_value = float(temp_str)
except ValueError:
    print("Помилка: потрібно ввести число.")
    exit()

# перевіряємо значення температури
if temp_value < 36:
    # знижена температура
    print("Температура знижена, можливе переохолодження")
elif temp_value <= 37:
    # норма
    print("Температура в межах норми")
else:
    # підвищена температура
    print("Підвищена температура, можлива гарячка")
    # рекомендація для > 38
    if temp_value > 38:
        print("Рекомендація: зверніться до лікаря")
